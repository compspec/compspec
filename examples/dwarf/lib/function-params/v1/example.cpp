namespace Example
{
    double Add(double a, double b)
    {
        return a + b;
    }

    double Remove(double a, double b)
    {
        return a - b;
    }

    double Change(double a, double b)
    {
        return a * b;
    }

    double ReturnChange(double a, double b)
    {
        return a + b;
    }
    double ReturnRemove(double a, double b)
    {
        return a + b;
    }
}
